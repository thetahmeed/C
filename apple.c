#include <stdio.h>

int main()
{
    // Calculation between two number a and b

    // 0. Declaring veriable
    int a = 10;
    int b = 20;

    // 1. Print and calculation
    printf("%d + %d = %d\n", a, b, a+b);
    printf("%d - %d = %d\n", a, b, a-b);
    printf("%d * %d = %d\n", a, b, a*b);
    printf("%d / %d = %d\n", a, b, a/b);

    return 0;
}